% build_channel_model.m — Programmatically creates channel_model.slx

mdl = 'channel_model';
if bdIsLoaded(mdl), close_system(mdl, 0); end
new_system(mdl);
open_system(mdl);

%% ---- Blocks ----
add_block('built-in/Inport', [mdl '/TX In'], ...
    'Position',[30 70 60 90]);

% Multipath (Rayleigh)
add_block('commblks/Multipath Rayleigh Fading Channel', [mdl '/Multipath'], ...
    'Position',[130 50 280 110], ...
    'SampleRate','10e6', ...
    'MaximumDopplerShift','5000', ...
    'PathDelays','[0 310e-9 710e-9 1090e-9 1730e-9 2510e-9]', ...
    'AveragePathGains','[0 -1 -9 -10 -15 -20]');

% Doppler (frequency shift via phase rotation)
add_block('built-in/MATLAB Function', [mdl '/Doppler Shift'], ...
    'Position',[340 50 460 110]);
set_param([mdl '/Doppler Shift'], 'MATLABFunction', ...
    ['function y = fcn(x)\n' ...
     'persistent k; if isempty(k), k=0; end\n' ...
     'n = length(x); t = (k:k+n-1)'' / 10e6;\n' ...
     'y = x .* exp(1j*2*pi*5000*t); k = k+n;']);

% AWGN
add_block('commblks/AWGN Channel', [mdl '/AWGN'], ...
    'Position',[520 50 640 110], ...
    'Mode','Signal to noise ratio (Eb/No)', ...
    'EbNo','10', ...
    'BitsPerSymbol','4');

add_block('built-in/Outport', [mdl '/RX Out'], ...
    'Position',[720 70 750 90]);

%% ---- Connections ----
add_line(mdl, 'TX In/1',       'Multipath/1');
add_line(mdl, 'Multipath/1',   'Doppler Shift/1');
add_line(mdl, 'Doppler Shift/1','AWGN/1');
add_line(mdl, 'AWGN/1',        'RX Out/1');

%% ---- Annotation ----
add_block('built-in/Note', [mdl '/Note1'], 'Position',[30 20 700 45]);
set_param([mdl '/Note1'], 'Text', ...
    'Channel: Multipath Rayleigh (ITU VehA) → Doppler (5kHz) → AWGN');

set_param(mdl, 'SolverType','Fixed-step', 'Solver','FixedStepDiscrete', ...
    'FixedStep','1/10e6', 'StopTime','1e-3');

save_system(mdl, fullfile(fileparts(mfilename('fullpath')), 'channel_model.slx'));
fprintf('Saved: channel_model.slx\n');
