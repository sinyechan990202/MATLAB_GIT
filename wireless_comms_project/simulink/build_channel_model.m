% build_channel_model.m — Programmatically creates channel_model.slx
% All channel effects implemented as MATLAB Function blocks (no toolbox dependency)

mdl = 'channel_model';
if bdIsLoaded(mdl), close_system(mdl, 0); end
new_system(mdl);
open_system(mdl);

lib  = 'simulink/User-Defined Functions/MATLAB Function';
px   = 30; step = 200;

%% Input
add_block('simulink/Sources/In1', [mdl '/TX In'], 'Position',[px 70 px+30 90]);
px = px + step;

%% Multipath Rayleigh (ITU VehA — implemented via MATLAB Function)
add_block(lib, [mdl '/Multipath'], 'Position',[px 55 px+140 105]);
set_fcn(mdl, 'Multipath', ...
    ['function y = fcn(x)' newline ...
     '% ITU Vehicular-A Rayleigh fading (6-tap)' newline ...
     'delays = [0 310 710 1090 1730 2510] * 1e-9;' newline ...
     'gains  = 10.^([0 -1 -9 -10 -15 -20]/20);' newline ...
     'fs     = 10e6;' newline ...
     'y      = zeros(size(x));' newline ...
     'for k = 1:length(delays)' newline ...
     '    d   = round(delays(k) * fs);' newline ...
     '    h   = (randn + 1j*randn) / sqrt(2);' newline ...
     '    xd  = [zeros(d,1); x(1:end-d)];' newline ...
     '    y   = y + gains(k) * h * xd;' newline ...
     'end']);
px = px + step;

%% Doppler Shift
add_block(lib, [mdl '/Doppler'], 'Position',[px 55 px+140 105]);
set_fcn(mdl, 'Doppler', ...
    ['function y = fcn(x)' newline ...
     '% Apply Doppler frequency shift (5 kHz — aviation scenario)' newline ...
     'persistent k' newline ...
     'if isempty(k), k = 0; end' newline ...
     'n = length(x);' newline ...
     't = (k : k+n-1)'' / 10e6;' newline ...
     'y = x .* exp(1j * 2*pi * 5000 * t);' newline ...
     'k = k + n;']);
px = px + step;

%% AWGN
add_block(lib, [mdl '/AWGN'], 'Position',[px 55 px+140 105]);
set_fcn(mdl, 'AWGN', ...
    ['function y = fcn(x, EbNo_dB)' newline ...
     '% Add AWGN given Eb/N0 (dB), BPS=4 (16-QAM)' newline ...
     'bps      = 4;' newline ...
     'EsNo     = EbNo_dB + 10*log10(bps);' newline ...
     'sig_pwr  = mean(abs(x).^2);' newline ...
     'noise_var = sig_pwr / (10^(EsNo/10));' newline ...
     'n = sqrt(noise_var/2) * (randn(size(x)) + 1j*randn(size(x)));' newline ...
     'y = x + n;']);
px = px + step;

%% Constant for EbNo input
add_block('simulink/Sources/Constant', [mdl '/EbNo'], ...
    'Value','10', 'SampleTime','inf', ...
    'Position',[px-step 155 px-step+80 185]);

%% Output
add_block('simulink/Sinks/Out1', [mdl '/RX Out'], 'Position',[px 70 px+30 90]);

%% Connections
add_line(mdl, 'TX In/1',    'Multipath/1');
add_line(mdl, 'Multipath/1','Doppler/1');
add_line(mdl, 'Doppler/1',  'AWGN/1');
add_line(mdl, 'EbNo/1',     'AWGN/2');
add_line(mdl, 'AWGN/1',     'RX Out/1');

set_param(mdl, 'SolverType','Fixed-step','Solver','FixedStepDiscrete', ...
    'FixedStep','1/10e6','StopTime','1e-3');

save_system(mdl, fullfile(fileparts(mfilename('fullpath')), 'channel_model.slx'));
fprintf('Saved: channel_model.slx\n');

function set_fcn(mdl, blk_name, script)
    rt = sfroot();
    ch = rt.find('-isa','Stateflow.EMChart','Path',[mdl '/' blk_name]);
    ch.Script = script;
end
